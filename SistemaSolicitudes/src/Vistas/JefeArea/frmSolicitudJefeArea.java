    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (cmbTecnicos.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Seleccione a un técnico");
        } else {
            sda = new SolicitudDAO();
            Solicitud obj = frmJefeArea.sol;
            obj.setEs(new Estado_solicitud(cmbEstado.getSelectedIndex() + 1));
            obj.setCat(new Categoria(cmbCategoria.getSelectedIndex() + 1));
            obj.setUsuarioTI(new UsuarioTI(cmbTecnicos.getSelectedIndex() + 1));
            obj.setPri(new Prioridad(cmbPrioridad.getSelectedIndex() + 1));
            obj.setDescripcion(txtDescripcion.getText());
            switch (obj.getEs().getIdEstado_solicitud()) {
                case 1:
                    sda.ModificarJF(obj);
                    break;
                case 2:
                    sda.atenderSolicitudJF(obj);
                    break;
                case 3:
                    sda.cierreSolicitudJF(obj);
                    break;
            }
            JOptionPane.showMessageDialog(null, "Se actualizó la solicitud correctamente");
            frmJefeArea frm = new frmJefeArea();
            frm.setVisible(true);
            frm.setUsuarioJ(this.usuarioJ);
            this.dispose();
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

