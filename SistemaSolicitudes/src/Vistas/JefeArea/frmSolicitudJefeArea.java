        cmbCategoria.setSelectedIndex(frmJefeArea.sol.getCat().getIdCategoria() - 1);
        cmbTecnicos.setSelectedIndex(frmJefeArea.sol.getUsuarioTI().getIdUsuarioTI() - 1);
